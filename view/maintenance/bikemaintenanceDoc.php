        <h3>Bike Maintenance Documentation</h3>
        <ol>
            <li><b>Once a Month:</b>
                <ol>
                    <li>Completely clean the bike, including the drivetrain if necessary</li>
                    <li>Inspect chain and freewheel. Measure the chain for wear, check for tight links and replace the chain if necessary</li>
                    <li>Inspect and lubricate brake levers, derailleurs and all cables</li>
                    <li>Inspect pedals and lubricate SPD style cleats. Inspect tires for wear; rotate or replace if needed</li>
                    <li>Inspect and check for looseness in the:
                        <ol>
                            <li>Cell phone holder</li>
                            <li>Stem binder bolt</li>
                            <li>Handlebar binder bolt </li>
                            <li>Seat-post binder bolt (or quick release)</li>
                            <li>Seat fixing bolt</li>
                            <li>Reflectors</li>
                            <li>Crank bolts</li>
                            <li>Chainring bolts</li>
                            <li>Derailleur mounting bolts</li>
                            <li>Bottle cage bolts</li>
                            <li>Rack mounting bolts</li>
                            <li>Brake and derailleur cable anchors</li>
                            <li>Brake and shifter lever mounting bolts</li>
                            <li>Brake mounting bolts</li>
                        </ol>
                    </li>
                </ol>
            </li>
            <br>
            <li><b>Every Three Months:</b>
                <ol>
                    <li>Inspect frame and fork for paint cracks or bulges that may indicate frame or
                        part damage; pay particular attention to all frame joints.</li>
                    <li>Visually inspect for bent components: seat rails, seat post, stem, handlebars,
                        chainrings, crankarms, brake calipers and brake levers.</li>
                </ol>
            </li>
            <br>
            <li><b>Every Six Months:</b>
                <ol>
                    <li>Inspect and readjust bearings in headset, hubs, pedals and bottom bracket (if possible;
                        some sealed cartridge bearings cannot be adjusted, only replaced).</li>
                    <li>Disassemble and overhaul; replace all bearings (if possible); and remove and if necessary replace all brake and shift cables.
                        This should be performed at 5,000 rentals, if this occurs more often than every six months.</li>
                </ol>
            </li>
        </ol>
    </div>
